package TestesDeAulas;
public class EntendoDados {
    public static void main(String[] args) {
        
        int var; //indica valores inteiros
        double x; // indica varivel com pontos flutuantes

        var = 10;
        x = 10.0;

        System.out.println("valor de var: " + var);
        System.out.println("valor de x: " + x);
        System.out.println();

        var = var/4;
        x = x/4;

        
        System.out.println("valor de var agora: " + var);
        System.out.println("valor de x agora: " + x);

    }
}

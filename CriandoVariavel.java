public class CriandoVariavel {
    public static void main(String[] args) {
        int var1;  //declaração de variavel 
        int var2;

        // sempre que declarar uma variavel deve seguir o seguinte padrão tipo nomedavariavel

        var1 = 1024; // atribuição de valores de variavel

        System.out.println("Valor da variavel 1: " + var1); // sinal de + diz que o valor vai ser exibido após a string 

        var2 = var1/2;  //existe alguns operadores matemáticos básicos: + - * /

        System.out.println("Varialvel 2 contém o valor de: ");
        System.out.println(var2);
    }
}
